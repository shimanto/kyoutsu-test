// 残りを生成して1000個にする
const EXTRA_TEMPLATES = [
  "あなたの{adj}さは{noun}級だ。",
  "{noun}もびっくりの{adj}さ。",
  "この{adj}さ、まさに{noun}。",
  "{adj}とはこのことだ。{noun}が証明している。",
  "世界一{adj}な受験生、ここにいる。",
];

const ADJECTIVES = [
  "完璧", "華麗", "天才的", "圧倒的", "輝かしい", "見事", "目覚ましい",
  "素晴らしい", "驚異的", "伝説的", "神がかった", "鮮やか", "冴えわたった",
  "堂々", "誇らしい", "無敵", "最強", "頼もしい", "力強い", "眩しい",
];

const NOUNS = [
  "東大首席", "ノーベル賞", "チャンピオン", "達人", "名人", "天才", "秀才",
  "マスター", "プロフェッショナル", "エキスパート", "レジェンド", "ヒーロー",
  "スペシャリスト", "グランドマスター", "鉄人", "仙人", "賢者", "知将",
  "軍師", "博士",
];

// テンプレートから残り分を生成
function generateExtra(): string[] {
  const result: string[] = [];
  let idx = 0;
  while (PRAISE.length + result.length < 1000) {
    const tmpl = EXTRA_TEMPLATES[idx % EXTRA_TEMPLATES.length];
    const adj = ADJECTIVES[(idx * 7) % ADJECTIVES.length];
    const noun = NOUNS[(idx * 3) % NOUNS.length];
    result.push(tmpl.replace("{adj}", adj).replace("{noun}", noun));
    idx++;
  }
  return result;
}

const ALL_PRAISE = [...PRAISE, ...generateExtra()];

/** ランダムな褒め言葉を1つ返す */
export function getRandomPraise(): string {
  return ALL_PRAISE[Math.floor(Math.random() * ALL_PRAISE.length)];
}

/** 複数の褒め言葉を重複なしで返す */
export function getRandomPraises(count: number): string[] {
  const shuffled = [...ALL_PRAISE].sort(() => Math.random() - 0.5);
  return shuffled.slice(0, count);
}

